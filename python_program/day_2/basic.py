
# Module with basic functions for simple calculator

# Addition
def add(num1, num2):
	'''add(x, y) -> value\n
Return the sum of x and y.'''

	return (num1 + num2)

# Subtraction
def sub(num1, num2):
	'''sub(x, y) -> value\n
Return the difference of subtracting y from x.'''

	return (num1 - num2)

# Multiplication
def mul(num1, num2):
	'''mul(x, y) -> value\n
Return the product of x and y.'''

	return (num1 * num2)

# Division
def div(num1, num2):
	'''div(x, y) -> value\n
Return the quotient of x divided by y.'''

	return (num1 / num2)

